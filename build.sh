#!/bin/bash
# ============================================================
#   MMY SvnGo - macOS 一键打包
# ============================================================

set -e

echo ""
echo "============================================================"
echo "  MMY SvnGo - macOS 一键打包"
echo "============================================================"
echo ""

cd "$(dirname "$0")"

# 检查 Python3
if ! command -v python3 &> /dev/null; then
    echo "[错误] 未找到 python3，请先安装 Python 3.10+"
    exit 1
fi

echo "[1/2] 检查依赖..."
python3 -m pip install -r requirements.txt --quiet

echo "[2/2] 开始打包..."
python3 build.py "$@"

echo ""
echo "============================================================"
echo "  打包完成! 请查看 release/ 目录"
echo "============================================================"
echo ""
